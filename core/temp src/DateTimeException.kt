package kotlinx.datetime

public class DateTimeException: RuntimeException {
    public constructor() : super()
    public constructor(p0: String?) : super(p0)
    public constructor(p0: String?, p1: Throwable?) : super(p0, p1)
    public constructor(p0: Throwable?) : super(p0)
    public constructor(p0: String?, p1: Throwable?, p2: Boolean, p3: Boolean) : super(p0, p1, p2, p3)
}